
//git
// ls - List files in the folder
// git status - Check which files are tracked


// git add <file> - Stage a file
// git add --all or git add -A - Stage all changes
// git status - See what is staged
// git restore --staged <file> - Unstage a file


// git commit -m "message" - Commit staged changes with a message
// git commit -a -m "message" - Commit all tracked changes (skip staging)
// git log - See commit history


// git stash - Stash your changes
// git stash push -m "message" - Stash with a message
// git stash list - List all stashes
// git stash branch <branchname> - Create a branch from a stash
// Show Stash Details (git stash show) See what was changed in the latest stash
// Apply the Latest Stash (git stash apply) Restore your most recent stashed changes (keeps the stash in the stack)
// Apply a Specific Stash (git stash apply stash@{n}) Restore a specific stash from the list
// Pop the Stash (git stash pop)Apply the latest stash and remove it from the stack
// Drop a Stash (git stash drop) Delete a specific stash when you no longer need it
// Clear All Stashes (git stash clear)Delete all your stashes at once:

// Branch from a Stash (git stash branch)Create a new branch and apply a stash to it.  Useful if your stashed work should become its own feature branch

